package classEx;

public class Man {
	String name;
	int age;
	boolean Married;
	int child;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public boolean isMarried() {
		return Married;
	}
	public void setMarried(boolean married) {
		Married = married;
	}
	public int getChild() {
		return child;
	}
	public void setChild(int child) {
		this.child = child;
	}	
}
